	private final long errorCode;
	private final String detailMessage;
	
	public WindowsException(long errorCode) {
		super(errorDescription.get(errorCode));
		
		this.errorCode = errorCode;
		this.detailMessage = errorDescription.get(errorCode);
	}
	
	public WindowsException(String message, long errorCode) {
		super(errorDescription.get(errorCode));
		
		this.errorCode = errorCode;
		this.detailMessage = message;
	}

	public long getCode() {
		return errorCode;
	}
	
	public String getErrorDescription() {
		return errorDescription.get(errorCode);
	}
	
	public String getDetailMessage() {
		return detailMessage;
	}
